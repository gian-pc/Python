#  Operadores Aritméticos

a = 10
b = 3

# Suma
suma = a + b
print(f'Suma de {a} + {b}: {suma}')

# Resta
resta = a - b
print(f'Resta de {a} - {b}: {resta}')

# Multiplicación
multiplicacion = a * b
print(f'Multiplicacion de {a} * {b}: {multiplicacion}')

# División (retorna un tipo float)
division = a / b
print(f'Division de {a} / {b}: {division:.2f}')

# División Entera
division_entera = a // b
print(f'Division entera {a} // {b}: {division_entera}')

# Modulo (residuo de la división)
modulo = a % b
print(f'Modulo de {a} % {b}: {modulo}')

# Exponente
exponente = a ** b
print(f'Exponente de {a} ** {b}: {exponente}')