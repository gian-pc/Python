# Ejemplo de tipos de datos en Python

# Entero
edad = 30
print(edad)

# Numero con puntos flotante
altura = 1.75
print('Altura:', altura)

# Cadena de texto
nombre = 'Ana'
print('Nombre:', nombre)

# Tipo Booleano
es_estudiante = True
print('Es estudiante:', es_estudiante)

# Tipo None, ausencia de valor
direccion = None
print('Direcci√≥n:', direccion)
