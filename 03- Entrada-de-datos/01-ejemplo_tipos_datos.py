# Ejemplo tipos datos

# Ejemplo de concatenación o suma de valores
numero1_cadena = '10'
numero2_cadena = '20'
print(f'Número en cadena: {numero1_cadena}')
print(f'Número en cadena: {numero2_cadena}')

suma = numero1_cadena + numero2_cadena
print(f'Valor suma de numeros: {suma}')

# Convertimos a tipos enteros
numero1_entero = int(numero1_cadena)
numero2_entero = int(numero2_cadena)
suma = numero1_entero + numero2_entero
print(f'Valor suma de numeros: {suma}')
