# Reemplazar subcadenas

cadena = 'Hola, mundo!'
print(f'Cadena original: {cadena}')  # Hola, mundo!
nueva_cadena =  cadena.replace('mundo', 'a todos')
print(f'Nueva cadena: {nueva_cadena}') # Hola, a todos!

# Sustituir hola por adios
nueva_cadena = cadena.replace('Hola', 'Adios')
print(f'Nueva cadena: {nueva_cadena}')  # Adios, mundo!
